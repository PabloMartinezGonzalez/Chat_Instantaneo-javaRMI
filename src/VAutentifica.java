    private void aceptar() throws RemoteException{
        try {
            int puerto = Integer.parseInt(RMIport.getText()); 
            String registryURL = 
              "rmi://"+hostNametxt.getText()+":"+ puerto + "/callback";  
            // find the remote object and cast it to an 
            //   interface object
            CallbackServerInterface servidor =  (CallbackServerInterface)Naming.lookup(registryURL);
            System.out.println("Lookup completed " );
            System.out.println("Server said " + servidor.sayHello());
            CallbackClientInterface cliente =  new CallbackClientImpl();
            // register for callback
            
            
            if(!textoUsuario.getText().isEmpty() && !txtClave.getText().isEmpty()){
            etiquetaFallo.setVisible(false);
            String usuario = servidor.comprobarAutentificacion(textoUsuario.getText(), txtClave.getText());
            
               
                if (usuario != null){

                    cliente.setId(usuario);
                    
                    
                    VPrincipal vp = new VPrincipal(cliente, servidor);
                    if(servidor.iniciarSesion(cliente, textoUsuario.getText(),  txtClave.getText())){
                        vp.setVisible(true);
                        System.out.println("Registered for callback.");
                        this.dispose();
                    }
                    else{
                        etiquetaConectado.setVisible(true);
                    }
                    
                    
                }else{
                    etiquetaFallo.setVisible(true);
                    
                }
            } else {
                
            }
            
            
            
            
            
          
            
          } // end try 
          catch (NumberFormatException | NotBoundException | MalformedURLException | RemoteException | SQLException e) {
            System.out.println(
              "Exception in CallbackClient: " + e);
          } // 
        
    }
    
