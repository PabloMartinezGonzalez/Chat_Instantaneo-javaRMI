private void aceptar(){
    try {
            int puerto = Integer.parseInt(puertotxt.getText()); 
            String registryURL = 
              "rmi://"+hosttxt.getText()+":" + puerto + "/callback";  
            // find the remote object and cast it to an 
            //   interface object
            CallbackServerInterface servidor =  (CallbackServerInterface)Naming.lookup(registryURL);
            System.out.println("Lookup completed " );
            System.out.println("Server said " + servidor.sayHello());
            CallbackClientInterface cliente =  new CallbackClientImpl();
            // register for callback
            
            
            if(!usuariotxt.getText().isEmpty() && !clavetxt.getText().isEmpty()){
                if(usuariotxt.getText().length()>=3){
                    avisotxt.setVisible(false);

                    if(servidor.registrarUsuario(cliente, usuariotxt.getText(), clavetxt.getText())){
                        cliente.setId(usuariotxt.getText());
                        VPrincipal vp = new VPrincipal(cliente, servidor);
                        vp.setVisible(true);
                        System.out.println("Registered for callback.");
                        this.dispose();
                    }
                    else{
                        avisotxt.setVisible(true);
                    }
                }
                else{
                    aviso2.setVisible(true);
                }
                
            }   
          } // end try 
          catch (NumberFormatException | NotBoundException | MalformedURLException | RemoteException e) {
            System.out.println(
              "Exception in CallbackClient: " + e);
          } // 
        
           // end try // end try 
          catch (Exception e) {
            System.out.println(
              "Exception in CallbackClient: " + e);
          } // 
}
