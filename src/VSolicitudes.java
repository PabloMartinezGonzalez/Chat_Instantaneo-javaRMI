    private void buscar(){
         ModeloTablaAmigos m3;
        m3 = (ModeloTablaAmigos) envSolicitudes.getModel();
        m3.setFilas(new ArrayList<>());
        envSolicitudes.setModel(m3);
        try {
            if(buscaramigo.getText().length()>=3){
                ArrayList<String> amigosbuscados = new ArrayList<>();
                amigosbuscados = servidor.buscarUsuariosSolictudes(buscaramigo.getText());
                if(amigosbuscados.contains(this.cliente.getId())){
                    amigosbuscados.remove(this.cliente.getId());
                }
                ArrayList<String> amigoseliminados = new ArrayList<>();
                for(String amig : amigosbuscados){
                    if(servidor.obtenerAmigos(cliente.getId(), "").contains(amig)){
                        amigoseliminados.add(amig);
                    }
                }
                for(String eliminado : amigoseliminados){
                    amigosbuscados.remove(eliminado);
                }
                amigoseliminados.clear();
                ModeloTablaAmigos m4;
                m4 = (ModeloTablaAmigos) envios.getModel();
                for(String amig : m4.devolver()){
                    if(amigosbuscados.contains(amig)){
                        amigoseliminados.add(amig);
                    }
                }
                for(String eliminado : amigoseliminados){
                    amigosbuscados.remove(eliminado);
                }
                m3.setFilas(amigosbuscados);
                aviso1.setVisible(false);
            }
            else{
                aviso1.setVisible(true);
            }
            envioSolicitud.setEnabled(false);
        } catch (SQLException | RemoteException ex) {
            Logger.getLogger(VSolicitudes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
