    private void envioSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_envioSolicitudActionPerformed
        // TODO add your handling code here:
        ModeloTablaAmigos m;
        ModeloTablaAmigos m2;
        m = (ModeloTablaAmigos) envSolicitudes.getModel();
        m2 = (ModeloTablaAmigos) envios.getModel();
        try{
            if(!buscaramigo.getText().isEmpty()){
                if(!servidor.existeUsuario(m.obtenerAmigos(envSolicitudes.getSelectedRow()))){
                    if(!servidor.obtenerAmigos(cliente.getId(), "").contains(m.obtenerAmigos(envSolicitudes.getSelectedRow()))){
                        servidor.hacerSolicitud(cliente.getId(), m.obtenerAmigos(envSolicitudes.getSelectedRow()));
                        m.setFilas(new ArrayList<>());
                        envSolicitudes.setModel(m);
                        m2.setFilas(servidor.misSolicitudes(cliente.getId()));
                        envios.setModel(m2);
                        buscaramigo.setText("");
                    }
                    else{
                        //fa.muestraExcepcion("Error: Ya es tu amigo");
                        System.out.println("Error: Ya es tu amigo");
                        return;
                    }
                }
                else{
                    //fa.muestraExcepcion("Error: No existe ese usuario");
                    System.out.println("Error: No existe ese usuario");
                    return;
                }
            }else{
                //fa.muestraExcepcion("Error: Tienes que introducir un nombre");
                System.out.println("Error: Tienes que introducir un nombre");
                return;
            }
        }catch (NumberFormatException e) {
            //fa.muestraExcepcion("Error: El nombre es incorrecto");
            System.out.println("Error: El nombre es incorrecto");
            return;
        } catch (SQLException ex) {
            Logger.getLogger(VSolicitudes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(VSolicitudes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_envioSolicitudActionPerformed

